function showConsultoria() {
    document.getElementById('profissionais-consultoria').style.display = 'block'
    document.getElementById('profissionais-servico').style.display = 'none'
}

function showServico() {
    document.getElementById('profissionais-consultoria').style.display = 'none'
    document.getElementById('profissionais-servico').style.display = 'block'
}